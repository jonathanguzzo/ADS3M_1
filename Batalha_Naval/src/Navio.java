public class Navio {
	private int tamanho;
	private String nome;

	public Navio(int tamanho, String nome) {
		super();
		this.tamanho = tamanho;
		this.nome = nome;
	}

	public int getTamanho() {
		return tamanho;
	}

	public void setTamanho(String nome) {
		this.tamanho = tamanho;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
}